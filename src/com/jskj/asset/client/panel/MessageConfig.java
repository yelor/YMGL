    public abstract void savedData(int days, boolean autorefresh, final int selectSec, boolean displayTask, boolean displayApplication);

    @Action
    public void save() {
        String date = jTextFieldDate.getText();
        Date selectDate = DateHelper.getStringtoDate(date, "yyyy-MM-dd");
        int days = DateHelper.diffDate(selectDate, new Date());
        boolean autorefresh = jCheckBoxAuto.isSelected();
        int selectSec = Integer.parseInt(jComboBoxSec.getSelectedItem().toString());
        logger.debug("calculation message data within " + days + " days");
        savedData(days, autorefresh, selectSec, jCheckBoxTask.isSelected(), jCheckBoxApp.isSelected());
        panel.hidePanel();

    }
