                javax.swing.GroupLayout layout = new javax.swing.GroupLayout(messageMain);
                messageMain.setLayout(layout);

                //这里构造对个JLABEL，这样可以就可以单行触发事件，有没有更好的方法？HTML似乎在这里不好用了？？？？
