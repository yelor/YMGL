    @Action
    public Task save() {
        if (depotName.getText().trim().equals("")) {
            AssetMessage.ERRORSYS("请输入仓库名称!");
            depotName.grabFocus();
            return null;
        }
        String zujima = PingYinUtil.getFirstSpell(depotName.getText().trim());
        appParam.setZujima(zujima);
        super.copyToBean(appParam, jPanel1);

        String userIdStr = jTextPID.getText();
        if (!userIdStr.trim().equals("")) {
            appParam.setUserId(Integer.parseInt(userIdStr));
        }

        String serviceId = "cangku/add";
        if (appParam.getDepotId() != null && appParam.getDepotId() > 0) {
            serviceId = "cangku/update";
        }

        return new CommUpdateTask<DepotALL>(appParam, serviceId) {
            @Override
            public void responseResult(ComResponse<DepotALL> response) {
                if (response.getResponseStatus() == ComResponse.STATUS_OK) {
                    parentPanel.reload().execute();
                    if (!jCheckBox2.isSelected()) {
                        close();
                    }
                } else {
                    AssetMessage.ERROR(response.getErrorMessage(), CangkuDialog.this);
                }
            }

        };

    }

